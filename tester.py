# 因为安卓端APK获取的视频流宽高比为3:4,为了与之一致，所以将宽高比限制为3:4
def check_image(image):
    height, width, channel = image.shape
    if width/height != 3/4:
        print(f"Aspect ratio: {width / height}")
        print("Image is not appropriate!!!\nHeight/Width should be 4/3.")
        return False
    else:
        return True

# Get bounding box in the form of x, y, w, h
def rearrange_bbox(src_w, src_h, bbox, scale):
        x = bbox[0]
        y = bbox[1]
        box_w = bbox[2]
        box_h = bbox[3]

        scale = min((src_h-1)/box_h, min((src_w-1)/box_w, scale))

        new_width = box_w * scale
        new_height = box_h * scale
        center_x, center_y = box_w/2+x, box_h/2+y

        left_top_x = center_x-new_width/2
        left_top_y = center_y-new_height/2
        right_bottom_x = center_x+new_width/2
        right_bottom_y = center_y+new_height/2

        if left_top_x < 0:
            right_bottom_x -= left_top_x
            left_top_x = 0

        if left_top_y < 0:
            right_bottom_y -= left_top_y
            left_top_y = 0

        if right_bottom_x > src_w-1:
            left_top_x -= right_bottom_x-src_w+1
            right_bottom_x = src_w-1

        if right_bottom_y > src_h-1:
            left_top_y -= right_bottom_y-src_h+1
            right_bottom_y = src_h-1

        return (int(left_top_x), int(left_top_y), int(new_width), int(new_height))



def resizeImage(image):
    offset = image.shape[0] % 4
    newHeight = int(image.shape[0] + offset)
    newWidth = int(3 * (newHeight) / 4)
    print(f"newHeight: {newHeight}, newWidth: {newWidth}")
    resized_image = cv2.resize(image, (newWidth, newHeight))
    return resized_image

def test(image, model_dir="./resources/anti_spoof_models", device_id=0):
    model_test = AntiSpoofPredict(device_id)
    image_cropper = CropImage()
    # image = cv2.imread(SAMPLE_IMAGE_PATH + image_name)
    # image = cv2.resize(image, (int(image.shape[0] * 3 / 4), image.shape[0]))
    image = resizeImage(image)
    result = check_image(image)
    if result is False:
        return
    image_bbox = model_test.get_bbox(image)
    prediction = np.zeros((1, 3))
    test_speed = 0
    # sum the prediction from single model's result
    for model_name in os.listdir(model_dir):
        h_input, w_input, model_type, scale = parse_model_name(model_name)
        param = {
            "org_img": image,
            "bbox": image_bbox,
            "scale": scale,
            "out_w": w_input,
            "out_h": h_input,
            "crop": True,
        }
        if scale is None:
            param["crop"] = False
        img = image_cropper.crop(**param)
        start = time.time()
        prediction += model_test.predict(img, os.path.join(model_dir, model_name))
        test_speed += time.time()-start


    print(f"inference speed: {test_speed}")
    # draw result of prediction
    label = np.argmax(prediction)
    value = prediction[0][label]/2

    print(f"label: {label}, value: {value}")

    img_height, img_width , _ = np.shape(image)
    new_bbox = rearrange_bbox(img_width, img_height, image_bbox, scale)

    return (label, value, new_bbox)


